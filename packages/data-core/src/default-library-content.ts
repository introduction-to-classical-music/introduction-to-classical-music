export function createDefaultLibraryArticles(now = new Date().toISOString()): Article[] {
  return [
    {
      id: "default-usage-guide",
      slug: "usage-guide",
      title: "不全书使用手册",
      summary: "面向安装版用户的完整上手说明，涵盖本地库、维护工具、自动检查、批量更新、专栏与构建发布流程。",
      markdown: buildDefaultUsageGuideMarkdown(),
      showOnHome: true,
      createdAt: now,
      updatedAt: now,
    },
  ];
}
