import path from "node:path";

import type { Article } from "./articles.js";

const usageGuideAssetBase = "/library-assets/managed/articles/usage-guide";

function usageGuideImage(fileName: string, alt: string, size: "medium" | "large" | "full" = "large") {
  return `![${alt}](${usageGuideAssetBase}/${fileName}){size=${size}}`;
}

export function getDefaultLibraryDocumentationSourceDir(repoRoot: string) {
  return path.join(path.resolve(repoRoot), "materials", "default-library", "usage-guide");
}

export function getDefaultLibraryDocumentationAssetDirSegments() {
  return ["articles", "usage-guide"] as const;
}

